   Estrutura inicial para Firebird 3.0 */

CREATE TABLE LOJAS (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    CODIGO VARCHAR(20) NOT NULL,
    NOME VARCHAR(120) NOT NULL,
    CIDADE VARCHAR(100),
    ENDERECO VARCHAR(250),
    REGIONAL VARCHAR(100),
    GERENTE VARCHAR(120),
    TELEFONE VARCHAR(30),
    EMAIL VARCHAR(150),
    HORARIO_FUNCIONAMENTO VARCHAR(100),
    STATUS VARCHAR(10) DEFAULT 'ATIVA' NOT NULL,
    CRIADO_EM TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    ATUALIZADO_EM TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_LOJAS PRIMARY KEY (ID),
    CONSTRAINT UK_LOJAS_CODIGO UNIQUE (CODIGO),
    CONSTRAINT CK_LOJAS_STATUS CHECK (STATUS IN ('ATIVA', 'INATIVA'))
);

CREATE TABLE FUNCIONARIOS (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    LOJA_ID BIGINT,
    MATRICULA VARCHAR(30) NOT NULL,
    NOME VARCHAR(150) NOT NULL,
    SETOR VARCHAR(100),
    CARGO VARCHAR(100),
    TURNO VARCHAR(50),
    TELEFONE VARCHAR(30),
    EMAIL VARCHAR(150),
    DATA_ADMISSAO DATE,
    NUMERO_ARMARIO VARCHAR(30),
    STATUS VARCHAR(15) DEFAULT 'ATIVO' NOT NULL,
    ORIGEM VARCHAR(10) DEFAULT 'MANUAL' NOT NULL,
    CRIADO_EM TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    ATUALIZADO_EM TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_FUNCIONARIOS PRIMARY KEY (ID),
    CONSTRAINT UK_FUNCIONARIOS_MATRICULA UNIQUE (MATRICULA),
    CONSTRAINT FK_FUNCIONARIOS_LOJA FOREIGN KEY (LOJA_ID) REFERENCES LOJAS (ID),
    CONSTRAINT CK_FUNCIONARIOS_STATUS CHECK (STATUS IN ('ATIVO', 'AFASTADO', 'FERIAS', 'INATIVO')),
    CONSTRAINT CK_FUNCIONARIOS_ORIGEM CHECK (ORIGEM IN ('RH', 'MANUAL'))
);

CREATE TABLE USUARIOS (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    FUNCIONARIO_ID BIGINT,
    NOME VARCHAR(150) NOT NULL,
    EMAIL VARCHAR(150) NOT NULL,
    MATRICULA VARCHAR(30),
    SENHA_HASH VARCHAR(255) NOT NULL,
    PERFIL VARCHAR(30) DEFAULT 'USUARIO' NOT NULL,
    ESCOPO VARCHAR(100),
    ATIVO BOOLEAN DEFAULT TRUE NOT NULL,
    ULTIMO_ACESSO_EM TIMESTAMP,
    CRIADO_EM TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    ATUALIZADO_EM TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_USUARIOS PRIMARY KEY (ID),
    CONSTRAINT UK_USUARIOS_EMAIL UNIQUE (EMAIL),
    CONSTRAINT UK_USUARIOS_FUNCIONARIO UNIQUE (FUNCIONARIO_ID),
    CONSTRAINT FK_USUARIOS_FUNCIONARIO FOREIGN KEY (FUNCIONARIO_ID) REFERENCES FUNCIONARIOS (ID),
    CONSTRAINT CK_USUARIOS_PERFIL CHECK (PERFIL IN ('ADMIN', 'GERENTE', 'RESPONSAVEL', 'USUARIO'))
);

CREATE TABLE CATEGORIAS (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    NOME VARCHAR(120) NOT NULL,
    TIPO VARCHAR(80),
    DESCRICAO VARCHAR(500),
    ATIVA BOOLEAN DEFAULT TRUE NOT NULL,
    CRIADO_EM TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    ATUALIZADO_EM TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_CATEGORIAS PRIMARY KEY (ID),
    CONSTRAINT UK_CATEGORIAS_NOME UNIQUE (NOME)
);

CREATE TABLE TIPOS_OCORRENCIA (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    CATEGORIA_ID BIGINT NOT NULL,
    NOME VARCHAR(180) NOT NULL,
    ATIVO BOOLEAN DEFAULT TRUE NOT NULL,
    CRIADO_EM TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    ATUALIZADO_EM TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_TIPOS_OCORRENCIA PRIMARY KEY (ID),
    CONSTRAINT UK_TIPOS_OCORRENCIA UNIQUE (CATEGORIA_ID, NOME),
    CONSTRAINT FK_TIPOS_CATEGORIA FOREIGN KEY (CATEGORIA_ID) REFERENCES CATEGORIAS (ID)
);

CREATE TABLE RESPONSAVEIS (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    FUNCIONARIO_ID BIGINT NOT NULL,
    LOJA_ID BIGINT,
    CATEGORIA_ID BIGINT,
    STATUS VARCHAR(15) DEFAULT 'DISPONIVEL' NOT NULL,
    PRINCIPAL BOOLEAN DEFAULT FALSE NOT NULL,
    CRIADO_EM TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    ATUALIZADO_EM TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_RESPONSAVEIS PRIMARY KEY (ID),
    CONSTRAINT UK_RESPONSAVEIS_ATRIBUICAO UNIQUE (FUNCIONARIO_ID, LOJA_ID, CATEGORIA_ID),
    CONSTRAINT FK_RESPONSAVEIS_FUNCIONARIO FOREIGN KEY (FUNCIONARIO_ID) REFERENCES FUNCIONARIOS (ID),
    CONSTRAINT FK_RESPONSAVEIS_LOJA FOREIGN KEY (LOJA_ID) REFERENCES LOJAS (ID),
    CONSTRAINT FK_RESPONSAVEIS_CATEGORIA FOREIGN KEY (CATEGORIA_ID) REFERENCES CATEGORIAS (ID),
    CONSTRAINT CK_RESPONSAVEIS_STATUS CHECK (STATUS IN ('DISPONIVEL', 'INDISPONIVEL', 'FERIAS'))
);

CREATE TABLE OCORRENCIAS (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    PROTOCOLO VARCHAR(30) NOT NULL,
    LOJA_ID BIGINT NOT NULL,
    CATEGORIA_ID BIGINT NOT NULL,
    RESPONSAVEL_ID BIGINT,
    CRIADO_POR_USUARIO_ID BIGINT,
    TITULO VARCHAR(250) NOT NULL,
    SETOR VARCHAR(100) NOT NULL,
    PRIORIDADE VARCHAR(10) DEFAULT 'MEDIA' NOT NULL,
    DESCRICAO BLOB SUB_TYPE TEXT NOT NULL,
    STATUS VARCHAR(15) DEFAULT 'ABERTA' NOT NULL,
    OCORRIDO_EM TIMESTAMP NOT NULL,
    RESOLVIDO_EM TIMESTAMP,
    CRIADO_EM TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    ATUALIZADO_EM TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_OCORRENCIAS PRIMARY KEY (ID),
    CONSTRAINT UK_OCORRENCIAS_PROTOCOLO UNIQUE (PROTOCOLO),
    CONSTRAINT FK_OCORRENCIAS_LOJA FOREIGN KEY (LOJA_ID) REFERENCES LOJAS (ID),
    CONSTRAINT FK_OCORRENCIAS_CATEGORIA FOREIGN KEY (CATEGORIA_ID) REFERENCES CATEGORIAS (ID),
    CONSTRAINT FK_OCORRENCIAS_RESPONSAVEL FOREIGN KEY (RESPONSAVEL_ID) REFERENCES RESPONSAVEIS (ID),
    CONSTRAINT FK_OCORRENCIAS_USUARIO FOREIGN KEY (CRIADO_POR_USUARIO_ID) REFERENCES USUARIOS (ID),
    CONSTRAINT CK_OCORRENCIAS_PRIORIDADE CHECK (PRIORIDADE IN ('BAIXA', 'MEDIA', 'ALTA', 'CRITICA')),
    CONSTRAINT CK_OCORRENCIAS_STATUS CHECK (STATUS IN ('ABERTA', 'EM_ANALISE', 'EM_ANDAMENTO', 'RESOLVIDA', 'CANCELADA'))
);

CREATE TABLE OCORRENCIA_TIPOS (
    OCORRENCIA_ID BIGINT NOT NULL,
    TIPO_OCORRENCIA_ID BIGINT NOT NULL,
    CONSTRAINT PK_OCORRENCIA_TIPOS PRIMARY KEY (OCORRENCIA_ID, TIPO_OCORRENCIA_ID),
    CONSTRAINT FK_OC_TIPOS_OCORRENCIA FOREIGN KEY (OCORRENCIA_ID) REFERENCES OCORRENCIAS (ID) ON DELETE CASCADE,
    CONSTRAINT FK_OC_TIPOS_TIPO FOREIGN KEY (TIPO_OCORRENCIA_ID) REFERENCES TIPOS_OCORRENCIA (ID)
);

CREATE TABLE OCORRENCIA_ANEXOS (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    OCORRENCIA_ID BIGINT NOT NULL,
    NOME_ARQUIVO VARCHAR(255) NOT NULL,
    TIPO_MIME VARCHAR(100),
    TAMANHO_BYTES BIGINT,
    CONTEUDO BLOB SUB_TYPE BINARY,
    CAMINHO_ARQUIVO VARCHAR(500),
    CRIADO_EM TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_OCORRENCIA_ANEXOS PRIMARY KEY (ID),
    CONSTRAINT FK_ANEXOS_OCORRENCIA FOREIGN KEY (OCORRENCIA_ID) REFERENCES OCORRENCIAS (ID) ON DELETE CASCADE,
    CONSTRAINT CK_ANEXOS_ORIGEM CHECK (CONTEUDO IS NOT NULL OR CAMINHO_ARQUIVO IS NOT NULL)
);

CREATE TABLE OCORRENCIA_HISTORICO (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    OCORRENCIA_ID BIGINT NOT NULL,
    USUARIO_ID BIGINT,
    ACAO VARCHAR(50) NOT NULL,
    STATUS_ANTERIOR VARCHAR(15),
    STATUS_NOVO VARCHAR(15),
    OBSERVACAO BLOB SUB_TYPE TEXT,
    CRIADO_EM TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_OCORRENCIA_HISTORICO PRIMARY KEY (ID),
    CONSTRAINT FK_HISTORICO_OCORRENCIA FOREIGN KEY (OCORRENCIA_ID) REFERENCES OCORRENCIAS (ID) ON DELETE CASCADE,
    CONSTRAINT FK_HISTORICO_USUARIO FOREIGN KEY (USUARIO_ID) REFERENCES USUARIOS (ID)
);

CREATE INDEX IX_FUNCIONARIOS_LOJA ON FUNCIONARIOS (LOJA_ID);
CREATE INDEX IX_FUNCIONARIOS_NOME ON FUNCIONARIOS (NOME);
CREATE INDEX IX_TIPOS_CATEGORIA ON TIPOS_OCORRENCIA (CATEGORIA_ID);
CREATE INDEX IX_RESPONSAVEIS_LOJA ON RESPONSAVEIS (LOJA_ID);
CREATE INDEX IX_RESPONSAVEIS_CATEGORIA ON RESPONSAVEIS (CATEGORIA_ID);
CREATE INDEX IX_OCORRENCIAS_LOJA ON OCORRENCIAS (LOJA_ID);
CREATE INDEX IX_OCORRENCIAS_CATEGORIA ON OCORRENCIAS (CATEGORIA_ID);
CREATE INDEX IX_OCORRENCIAS_RESPONSAVEL ON OCORRENCIAS (RESPONSAVEL_ID);
CREATE INDEX IX_OCORRENCIAS_STATUS ON OCORRENCIAS (STATUS);
CREATE INDEX IX_OCORRENCIAS_DATA ON OCORRENCIAS (OCORRIDO_EM);
CREATE INDEX IX_ANEXOS_OCORRENCIA ON OCORRENCIA_ANEXOS (OCORRENCIA_ID);
CREATE INDEX IX_HISTORICO_OCORRENCIA ON OCORRENCIA_HISTORICO (OCORRENCIA_ID);

COMMIT;
